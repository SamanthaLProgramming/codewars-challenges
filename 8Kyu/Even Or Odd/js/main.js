// Even or Odd


// My Solution

function evenOrOdd(number) {
    if (number % 2 === 0) {
      return "Even";
    }
    else {
      return "Odd"
    }
}




// Other Solutions

function evenOrOdd(number) {
    return number % 2 === 0 ? 'Even' : 'Odd';
}


